fun main() {

    val myByte: Byte = 100
    val myShort: Short = 4000
    val myInt: Int = 70000
    val myLong: Long = 14000000000
    val myFloat: Float = 2.99F
    val myDouble: Double = 29.99
    val myBoolean: Boolean = true
    val myChar: Char = 'A'


    println("Byte: $myByte")
    println("Short: $myShort")
    println("Int: $myInt")
    println("Long: $myLong")
    println("Float: $myFloat")
    println("Double: $myDouble")
    println("Boolean: $myBoolean")
    println("Char: $myChar")

}
